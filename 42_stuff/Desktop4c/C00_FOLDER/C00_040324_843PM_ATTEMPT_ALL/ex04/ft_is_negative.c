#include	<unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	nn;

	p = 'P';
	nn = 'N';
	if (n < 0)
	{
		write(1, &nn, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
